export function determineSection(title: string): string {
  for (const [section, resources] of Object.entries(
    RESOURCE_SECTIONS
  )) {
    if ((resources as readonly string[]).includes(title)) {
      return section;
    }
  }
  return 'Other';
}

export function getResourceIcon(title: string): string {
  return ICON_MAP[title] || 'material-symbols:list';
}

export function getResourcesBySection(
  section: string
): readonly string[] {
  return (
    (RESOURCE_SECTIONS[
      section as keyof typeof RESOURCE_SECTIONS
    ] as readonly string[]) || []
  );
}

export function getAllSections(): string[] {
  return Object.keys(RESOURCE_SECTIONS);
}
